// 创建不同类型的变量

#include <iostream>
#include <string>

using namespace std;

int main()
{
    int num = 9;
    float f = 5.455;
    double d = 9.00032;
    char ch = 'a';
    string str = "china";
    bool b = true;

    cout << "int " << num << endl;
    cout << "float " << f << endl;
    cout << "double " << d << endl;
    cout << "char " << ch << endl;
    cout << "string " << str << endl;
    cout << "bool " << b << endl;

    return 0;
}